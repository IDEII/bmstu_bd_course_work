CREATE TRIGGER trg_prevent_delete_request
ON REQUEST
INSTEAD OF DELETE
AS
BEGIN
    DECLARE @fighter_id uniqueidentifier;

    DECLARE delete_cursor CURSOR FOR
    SELECT FIGHTER_ID
    FROM DELETED;

    OPEN delete_cursor;
    FETCH NEXT FROM delete_cursor INTO @fighter_id;

    WHILE @@FETCH_STATUS = 0
    BEGIN
        IF EXISTS (SELECT 1 FROM SELECTED_MEMBER WHERE FIGHTER_ID = @fighter_id)
        BEGIN
            RAISERROR('Удаление записи запрещено, так как боец связан с выбранным участником.', 16, 1);
            CLOSE delete_cursor;
            DEALLOCATE delete_cursor;
            RETURN;
        END

        FETCH NEXT FROM delete_cursor INTO @fighter_id;
    END

    CLOSE delete_cursor;
    DEALLOCATE delete_cursor;

    DELETE FROM REQUEST
    WHERE REQUEST_ID IN (SELECT REQUEST_ID FROM DELETED);
END;
GO

