CREATE TRIGGER trg_LimitTournamentParticipants
ON SELECTED_MEMBER
INSTEAD OF INSERT
AS
BEGIN
    DECLARE @tournamentID uniqueidentifier;
    DECLARE @maxParticipants INT;
    DECLARE @currentCount INT;

    SELECT @tournamentID = TOURNAMENT_ID FROM INSERTED;
	SELECT @maxParticipants = POWER(2, ROUNDS_NUMBER) FROM TOURNAMENT WHERE TOURNAMENT_ID = @tournamentID;
    SELECT @currentCount = COUNT(*) 
    FROM SELECTED_MEMBER 
    WHERE TOURNAMENT_ID = @tournamentID;

    DECLARE @requestID uniqueidentifier;
    DECLARE @fighterID uniqueidentifier;

    DECLARE cursor_selected CURSOR FOR
    SELECT REQUEST_ID, FIGHTER_ID FROM INSERTED;

    OPEN cursor_selected;
    FETCH NEXT FROM cursor_selected INTO @requestID, @fighterID;

    WHILE @@FETCH_STATUS = 0
    BEGIN
        IF (@currentCount < @maxParticipants)
        BEGIN
            INSERT INTO SELECTED_MEMBER (REQUEST_ID, FIGHTER_ID, TOURNAMENT_ID)
            VALUES (@requestID, @fighterID, @tournamentID);

            SET @currentCount = @currentCount + 1; 
        END
        ELSE
        BEGIN
            RAISERROR ('Невозможно добавить участника: максимальное количество участников достигнуто.', 16, 1);
            BREAK;
        END

        FETCH NEXT FROM cursor_selected INTO @requestID, @fighterID;
    END

    CLOSE cursor_selected;
    DEALLOCATE cursor_selected;
END;
GO

CREATE TRIGGER trg_InsertRequest
ON REQUEST
INSTEAD OF INSERT
AS
BEGIN
    DECLARE @fighterID uniqueidentifier;
    DECLARE @tournamentID uniqueidentifier;
    DECLARE @invitedParticipantsID uniqueidentifier;

    SELECT @fighterID = FIGHTER_ID, 
           @tournamentID = TOURNAMENT_ID 
    FROM INSERTED;

    IF @fighterID <> '00000000-0000-0000-0000-000000000000'
    BEGIN
        IF EXISTS (
            SELECT 1 
            FROM REQUEST 
            WHERE FIGHTER_ID = @fighterID 
              AND TOURNAMENT_ID = @tournamentID
        )
        BEGIN
            RAISERROR ('Невозможно добавить заявку: боец уже зарегистрирован на этот турнир.', 16, 1);
            RETURN;
        END
    

    SELECT @invitedParticipantsID = INVITED_PARTICIPANTS_ID 
    FROM INVITED_PARTICIPANTS 
    WHERE FIGHTER_ID = @fighterID AND TOURNAMENT_ID = @tournamentID;

    IF @invitedParticipantsID IS NOT NULL
    BEGIN
        INSERT INTO REQUEST (REQUEST_ID, FIGHTER_ID, TOURNAMENT_ID, INVITED_PARTICIPANTS_ID, DECISION)
        SELECT REQUEST_ID, FIGHTER_ID, TOURNAMENT_ID, @invitedParticipantsID, 1  -- Положительное решение
        FROM INSERTED;
    END
    ELSE
    BEGIN
        INSERT INTO REQUEST (REQUEST_ID, FIGHTER_ID, TOURNAMENT_ID, DECISION)
        SELECT REQUEST_ID, FIGHTER_ID, TOURNAMENT_ID, 0  -- Негативное решение
        FROM INSERTED;
    END
	END
END
GO

CREATE TRIGGER trg_PreventDuplicateMatchNumbers
ON MATCH_TABLE
INSTEAD OF INSERT
AS
BEGIN
    DECLARE @tournamentID uniqueidentifier;
    DECLARE @matchNumber INT;

    SELECT 
