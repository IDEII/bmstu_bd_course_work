ON INVITED_PARTICIPANTS
INSTEAD OF INSERT
AS
BEGIN
    IF EXISTS (
        SELECT 1
        FROM INVITED_PARTICIPANTS AS ip
        JOIN inserted AS i
        ON ip.FIGHTER_ID = i.FIGHTER_ID 
        AND ip.TOURNAMENT_ID = i.TOURNAMENT_ID
    )
    BEGIN
        RAISERROR('Приглашение уже существует для данного бойца на этот турнир.', 16, 1);
