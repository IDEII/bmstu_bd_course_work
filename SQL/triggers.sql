        TOP_PARTICIPANT_ID, 
        BOTTOM_PARTICIPANT_ID, 
        WHEN_PLAYED, 
        WINNER_ID, 
        TOP_SCORE, 
        BOTTOM_SCORE 
    FROM INSERTED;
END;
GO


CREATE TRIGGER trg_AfterDelete_Fighter
ON FIGHTER
INSTEAD OF DELETE
AS
BEGIN
    DECLARE @DeletedFighterID UNIQUEIDENTIFIER;
    DECLARE @DefaultFighterID UNIQUEIDENTIFIER = '00000000-0000-0000-0000-000000000000';  -- Замените на фактический ID

    SELECT @DeletedFighterID = FIGHTER_ID FROM DELETED;

    IF NOT EXISTS (SELECT 1 FROM SELECTED_MEMBER WHERE FIGHTER_ID = @DeletedFighterID)
    BEGIN
        DELETE FROM REQUEST
        WHERE REQUEST_ID IN (
            SELECT r.REQUEST_ID
            FROM REQUEST r
            LEFT JOIN SELECTED_MEMBER sm ON r.REQUEST_ID = sm.REQUEST_ID
            GROUP BY r.REQUEST_ID
            HAVING COUNT(sm.PARTICIPANT_ID) = 0
        );

        DELETE FROM INVITED_PARTICIPANTS
        WHERE FIGHTER_ID = @DeletedFighterID;
    END
    ELSE
    BEGIN
        UPDATE REQUEST
        SET FIGHTER_ID = @DefaultFighterID
        WHERE FIGHTER_ID = @DeletedFighterID;

        UPDATE INVITED_PARTICIPANTS
        SET FIGHTER_ID = @DefaultFighterID
        WHERE FIGHTER_ID = @DeletedFighterID;

        UPDATE SELECTED_MEMBER
        SET FIGHTER_ID = @DefaultFighterID
        WHERE FIGHTER_ID = @DeletedFighterID;
    END 

    DELETE FROM FIGHTER
    WHERE FIGHTER_ID = @DeletedFighterID;
END;
GO

